package classroomSix;

public class Calculator {

    public int sum (int a, int b){
        return a+b;
    }

    public int multiply (int a, int b){
        return a*b;
    }

    public int addition(int x, int y) {
        return x + y;
    }

    public int subtraction(int x, int y) {
        return x - y;
    }

    public float divide(int x, int y) {
        return x / y;
    }
}
